_REGION_TO_DISTRICT: dict[str, str] = {}
for district, regions in DISTRICTS.items():
    for r in regions:
        _REGION_TO_DISTRICT[r.lower()] = district


def district_of(region: str | None) -> str | None:
    if not region:
        return None
    raw = region.strip().lower()
    if raw in _REGION_TO_DISTRICT:
        return _REGION_TO_DISTRICT[raw]
    for known, district in _REGION_TO_DISTRICT.items():
        if known in raw or raw in known:
            return district
    return None


